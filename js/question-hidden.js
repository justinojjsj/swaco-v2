/*
QUESTAO ALERGICO
*/

document.getElementById('q-allergies-hidden').style.display = 'none';

$('#q-allergies-yes').click(function() {      
    $('#q-allergies').animate({height : "300px", }, 200);
    document.getElementById('q-allergies-hidden').style.display = 'block';        
});

$('#q-allergies-no').click(function() {      
    $('#q-allergies').animate({height : "150px", }, 200); 
    document.getElementById('q-allergies-hidden').style.display = 'none';       
});

